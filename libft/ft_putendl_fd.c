#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int		i;
	char	c;

	i = 0;
	while (s[i] != '\0')
		ft_putchar_fd(s[i++], fd);
	c = '\n';
	ft_putchar_fd(c, fd);
}
