#include "libft.h"

int		ft_numlen(int n)
{
	int		res;
	long	a;

	res = 0;
	a = n;
	if (a < 0)
		a *= -1;
	while (a > 0)
	{
		a = a / 10;
		res++;
	}
	return (res);
}
